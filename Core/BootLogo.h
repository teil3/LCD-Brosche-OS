// Bootlogo zeichnen (zentriert) und optional für `ms` anzeigen
inline void drawBootLogo(TFT_eSPI& tft, uint32_t ms) {
  tft.fillScreen(TFT_BLACK);

  const int x = (int)((int)TFT_W - (int)SCHMUCKLOGO_WIDTH)  / 2 - 3;
  const int y = (int)((int)TFT_H - (int)SCHMUCKLOGO_HEIGHT) / 2;

  tft.startWrite();
  TJpgDec.drawJpg(x, y, schmucklogo_jpg, sizeof(schmucklogo_jpg));
  tft.endWrite();

  const uint32_t t0 = millis();
  while (millis() - t0 < ms) { delay(10); yield(); }
}

